#include<stdio.h>
int main()
{
    float a = 15;
    int b = 2;
    int sum = a + b;
    printf("Summation = %d\n", sum);
    int sub = a - b;
    printf("Subtraction = %d\n", sub);
    int mul = a * b;
    printf("Multiplication = %d\n", mul);

    float div = a / b;
    printf("Divided = %f\n", div);
    return 0;
}